/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void LedBlinkTask(void const * argument)
{
  uint8_t ledBlinking;

  printf("LedBlinkTask started\n");
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
    xSemaphoreTake(semaLedBlinking, portMAX_DELAY);
    ledBlinking = gLedBlinking;
    xSemaphoreGive(semaLedBlinking);

    if (ledBlinking)
    {
      HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
      osDelay(1000);
    }
    else
    {
      osDelay(100);
    }
  }
  /* USER CODE END 5 */
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == B1_Pin)
  {
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;
    uint8_t ledBlinking;

    xSemaphoreTakeFromISR(semaLedBlinking, &xHigherPriorityTaskWoken);
    gLedBlinking = !gLedBlinking;
    ledBlinking = gLedBlinking;
    xSemaphoreGiveFromISR(semaLedBlinking, &xHigherPriorityTaskWoken);

    if (!ledBlinking) {
      printf("Button pressed, led stop blinking.\n");
      if (HAL_GPIO_ReadPin(LD2_GPIO_Port, LD2_Pin) == GPIO_PIN_SET)
      {
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
      }
    } else {
      printf("Button pressed, led start blinking.\n");
    }

    portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
  }
}

